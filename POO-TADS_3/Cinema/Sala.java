package Cinema;

public class Sala {
   
    private String nome;
    private int numero;
    
    
    public Sala(){}    
    
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public int getNumero() {
        return numero;
    }
    public void setNumero(int numero) {
        this.numero = numero;
    }


   

    
}